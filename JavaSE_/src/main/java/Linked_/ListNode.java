/**
 * @ClassName ListNode
 * @Description
 * @Author Four5prings
 * @Date 2022/5/1 12:10
 * @Version 1.0
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode() {
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
