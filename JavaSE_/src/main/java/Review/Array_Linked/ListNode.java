package Review.Array_Linked;

/**
 * @ClassName ListNode
 * @Description
 * @Author Four5prings
 * @Date 2022/5/2 20:23
 * @Version 1.0
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

    public ListNode(int val) {
        this.val = val;
    }
}
